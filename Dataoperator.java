public class Dataoperator {
    public static void main(String[] args) {
         int a = 68;
        int b = 22;

        System.out.println(a + b); 
        System.out.println(a - b); 
        System.out.println(a * b); 
        System.out.println(a / b); 
        System.out.println(a % b);
    }
}
